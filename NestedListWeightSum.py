class Solution(object):
    def depthSum(self, nestedList):
        """
        :type nestedList: List[NestedInteger]
        :rtype: int
        """
        stk = []
        sum = 0
        for ni in nestedList:
            stk.append((ni,1))
        while stk:
            ni, d = stk.pop()
            if ni.isInteger():
                sum += d*ni.getInteger()
            else:
                for nni in ni.getList():
                    stk.append((nni,d+1))
                    
        return sum
        